def _clean(text):
    # collapse whitespace; keep one space between words
    return re.sub(r"\s+", " ", text.strip())


def op(family, title, text, tags, min_version="all", aliases=None, source=D_OC):
    return {
        "family": family,
        "title": title,
        "category": "operator",
        "source": source,
        "min_version": min_version,
        "tags": tags,
        "aliases": aliases or [],
        "text": _clean(text),
    }


