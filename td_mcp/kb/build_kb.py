def main():
    # Merge the merged MIT operator/Python corpus into the curated seeds so the
    # retriever benefits from 900+ operators + classes with version stamps.
    try:
        from td_mcp.kb.corpus import corpus_chunks
        RECORDS.extend(corpus_chunks())
    except Exception:  # noqa: BLE001
        pass

    # assign stable ids (records may already carry an explicit 'id')
    seen = {}
    out = []
    for r in RECORDS:
        if r.get("id"):
            cid = base = r["id"]
        else:
            base = re.sub(r"[^a-z0-9]+", "_", r["title"].lower()).strip("_")
            cid = base
        i = 1
        while cid in seen:
            i += 1
            cid = f"{base}_{i}"
        seen[cid] = True
        rec = {"id": cid}
        rec.update(r)
        out.append(rec)

    with open(OUT, "w", encoding="utf-8") as fh:
        for rec in out:
            fh.write(json.dumps(rec, ensure_ascii=False) + "\n")

    from collections import Counter
    fam = Counter((r["family"] or r["category"]) for r in out)
    print(f"Wrote {len(out)} chunks -> {OUT}")
    for k, v in sorted(fam.items(), key=lambda x: -x[1]):
        print(f"  {k:10s} {v}")


if __name__ == "__main__":
    main()
